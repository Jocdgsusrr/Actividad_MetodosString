
class StringMetodos
    # CortaNumDePalabras
    def cortar_string(str)
      str.split.size
    end
  
    # InvierteCadena
    def invertir_string(str)
      str.reverse
    end
  
    # Mayusculas
    def mayus_string(str)
      str.upcase
    end
  end

